package Encapsulamiento.paquete1;

public class Clase1 {
     private int edad;
     private String nombre;

     // Set y Get (setters y getters)

     // set - establecemos edad
     public void setEdad(int edad) {
          this.edad = edad;
     }

     // get - mostramos la edad
     public int getEdad() {
          return edad;
     }

     public void setNombre(String nombre) {
          this.nombre = nombre;
     }
     public String getNombre() {
          return nombre;
     }
}
